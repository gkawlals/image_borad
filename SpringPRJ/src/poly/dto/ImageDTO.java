package poly.dto;

public class ImageDTO {
	
	private String image_no;
	private String coment;
	private String image_url;
	private String image_name;
	private String user_id;
	private String reg_id;
	private String chg_id;
	private String like;
	private String circle_cnt;
	
	public String getImage_no() {
		return image_no;
	}
	public void setImage_no(String image_no) {
		this.image_no = image_no;
	}
	public String getComent() {
		return coment;
	}
	public void setComent(String coment) {
		this.coment = coment;
	}
	public String getImage_url() {
		return image_url;
	}
	public void setImage_url(String image_url) {
		this.image_url = image_url;
	}
	public String getImage_name() {
		return image_name;
	}
	public void setImage_name(String image_name) {
		this.image_name = image_name;
	}
	public String getUser_id() {
		return user_id;
	}
	public void setUser_id(String user_id) {
		this.user_id = user_id;
	}
	public String getReg_id() {
		return reg_id;
	}
	public void setReg_id(String reg_id) {
		this.reg_id = reg_id;
	}
	public String getChg_id() {
		return chg_id;
	}
	public void setChg_id(String chg_id) {
		this.chg_id = chg_id;
	}
	public String getLike() {
		return like;
	}
	public void setLike(String like) {
		this.like = like;
	}
	public String getCircle_cnt() {
		return circle_cnt;
	}
	public void setCircle_cnt(String circle_cnt) {
		this.circle_cnt = circle_cnt;
	}


}
